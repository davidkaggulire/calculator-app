"""calculator.py"""


def add_function(first_num, second_num):
    """function adds two numbers"""
    addition = first_num + second_num
    return addition


def subtract_function(first_num, second_num):
    """function subtracts two numbers"""
    difference = first_num-second_num
    return difference


def division_function(first_num, second_num):
    """function divides two numbers"""
    dividend = first_num / second_num
    return dividend


def multiplication_function(first_num, second_num):
    """function multiplies two numbers"""
    product = first_num * second_num
    return product


def remainder_function(first_num, second_num):
    """function to give the remainder of two numbers"""
    remainder = first_num % second_num
    return remainder
